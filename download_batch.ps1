if (!(Test-Path "heroes")) { New-Item -ItemType Directory -Path "heroes" }

foreach ($h in $heroMap.Keys) {
    $filename = $h.ToLower().Replace(" ", "_").Replace("-", "_").Replace(".", "_").Replace("'", "") + ".png"
    $path = "heroes/$filename"
    $url = $heroMap[$h]

    if (Test-Path $path) {
        Write-Host "Saltando $h (ya existe)."
        continue
    }

    Write-Host "Descargando $h..."
    try {
        # Intento directo con wsrv.nl proxy (ms fiable para evitar bloqueos)
        $proxyUrl = "https://wsrv.nl/?url=" + [Uri]::EscapeDataString($url)
        Invoke-WebRequest -Uri $proxyUrl -OutFile $path -TimeoutSec 15 -ErrorAction Stop
    } catch {
        Write-Host "Reintentando descarga directa para $h..."
        try {
            Invoke-WebRequest -Uri $url -OutFile $path -TimeoutSec 15 -ErrorAction Stop
        } catch {
             Write-Host "Error fatal descargando $h"
        }
    }
}
